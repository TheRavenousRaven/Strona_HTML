function lyrics1() {
    var rozw = document.getElementById("rozw");
    var moreText = document.getElementById("tekst-kiedy");
    var btnText = document.getElementById("przycisk");

    if (rozw.style.display === "none") {
        rozw.style.display = "inline";
        btnText.innerHTML = "Poka¿ tekst";
        moreText.style.display = "none";
    }
    else {
        rozw.style.display = "none";
        btnText.innerHTML = "Ukryj tekst";
        moreText.style.display = "inline";
    }
}

